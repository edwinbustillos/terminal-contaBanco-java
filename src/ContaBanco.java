public class ContaBanco {
    int numero;
    String agencia;
    String nomeCliente;
    double saldo;

    public ContaBanco(int numero, String agencia, String nomeCliente, double saldo) {
        this.numero = numero;
        this.agencia = agencia;
        this.nomeCliente = nomeCliente;
        this.saldo = saldo;
    }
}